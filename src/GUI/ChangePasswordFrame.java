    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(validateInputs()==true)
        {
            try{
                int ans=UsersDAO.changePassword(txtUserId.getText(),String.valueOf(txtConfirmPass.getPassword()));
                if(ans==1)
                {
                    JOptionPane.showMessageDialog(null,"Password changed successfully","Success",JOptionPane.INFORMATION_MESSAGE);
                    StudentOptionsFrame sof=new StudentOptionsFrame();
                    sof.setVisible(true);
                    this.dispose();
                }
                else
                    JOptionPane.showMessageDialog(null,"Password not changed successfully","Error",JOptionPane.ERROR_MESSAGE);
            }
            catch(SQLException e)
            {
                JOptionPane.showMessageDialog(null,"SQL Exception"+e,"failed",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

