    private boolean validateInputs() {
        String pass=String.valueOf(txtPass.getPassword());
        String confirmPass=String.valueOf(txtConfirmPass.getPassword());
        if(pass.equals(confirmPass))
        {
            if(pass.length()>=4)
                return true;
            else
            {
                JOptionPane.showMessageDialog(null,"Length of Password should be atleast 4 characters long","failed",JOptionPane.ERROR_MESSAGE);
                return false;
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Passwords did not match","failed",JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }
