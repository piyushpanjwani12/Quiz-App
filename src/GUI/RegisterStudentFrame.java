    private int validateInputs() {
       if(txtUserId.getText().isEmpty() || txtPass.getPassword()==null || txtConfirmPass.getPassword()==null)
       {
           return 0;
       }
       else if(txtUserId.getText().length()<4 || String.valueOf(txtPass.getPassword()).length()<4)
       {
            System.out.println(txtUserId.getText().length());
           return -1;
       }
       else
           return 1;
    }

