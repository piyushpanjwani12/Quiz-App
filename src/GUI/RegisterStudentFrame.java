    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        if(validateInputs()==0)
        {
            JOptionPane.showMessageDialog(null,"Please input all the Fields","ERROR",JOptionPane.ERROR_MESSAGE);
        }
        else if(validateInputs()==-1)
        {
            JOptionPane.showMessageDialog(null,"User id and password should be greater 4 characters or more","ERROR",JOptionPane.ERROR_MESSAGE);
        }
        else{
            if(String.valueOf(txtPass.getPassword()).equals(String.valueOf(txtConfirmPass.getPassword())))
            {
                try{
                    Users u=new Users();
                    u.setUserId(txtUserId.getText());
                    u.setUserPass(String.valueOf(txtPass.getPassword()));
                    u.setUserType("student");
                    int ans=UsersDAO.registerNewStudent(u);
                    if(ans==1)
                    {
                        JOptionPane.showMessageDialog(null,"Student Entered Successfully","Hurray !!",JOptionPane.PLAIN_MESSAGE);
                        clearAll();
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(null,"Error Occured, Try Again","ERROR",JOptionPane.ERROR_MESSAGE);
                    }
                }
                catch(SQLException e)
                {
                    JOptionPane.showMessageDialog(null,"SQL Exception:"+e,"ERROR",JOptionPane.ERROR_MESSAGE);
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Passwords Did not match","ERROR",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

